public class SwitchTest {
    public static void main(String[] args) {
        String param="null";
        switch (param){
            case "param":
                System.out.println("param");
                break;
            case "String":
                System.out.println("String");
                break;
            case "null":
                System.out.println("null");
                break;
            default:
                System.out.println("default");

        }
    }
}